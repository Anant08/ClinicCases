<p>ClinicCases is web-based case management system specifically designed for live-client law school clinics.  It is <a href="http://en.wikipedia.org/wiki/FOSS" alt="Wikipedia on FOSS" title="Wikipedia on FOSS" target="_new">free and open source software</a>.</p>
<br>
<h3>Features</h3>
<ul>
<li><b>Easy to Use</b> - The guiding principle behind this project is that this software can be used easily by anyone with a rudimentary familiarity with the internet.  No day-long trainings.  No phonebook-sized users manuals.</li>
<li><b>Free and Open Source</b> - ClinicCases is offered free of charge to all users.  We encourage all developers to contribute their ideas to this project!  If you need it, <a href="http://www.threepipeproblem.com">Three Pipe Problem</a> will offer hosting, technical support, customization, and data migration from your current case management system.</li>
<li><b>Customizable</b> - ClinicCases can be configured to meet the specific needs of your law school clinic.</li>
<li><b>Your Students' Work At Your Fingertips</b> - Professors can logon and see up-to-the-second information on student activity, compare student work, and critique and edit student documents.</li>
<li><b>Easy Student and Case Management</b> - Cases can be assigned to students using a simple drag and drop interface.  Adminstrators can open and close cases at the touch of a button.</li>
<li><b>Security and Reliability</b> - All data is tranfserred to a secure server via SSL.  All aspects of a case - case notes, messages, documents, are permanently stored and available via a simple search.</li>
<li><b>Journaling</b> - Journaling feature allows your students to submit journals and for you to read them in one centralized location.  No more "My Journal" emails!  Latest journals are available to you via your favorite RSS reader.</li>
<li><b>Mobile</b> - Access your case information anywhere via a web-enabled mobile phone or PDA.</li>
<li>and much more....</li>
</ul> 
